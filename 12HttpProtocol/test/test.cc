#include <iostream>

int main()
{
    bool b = 1;
    std::cout << b << std::endl;

    b = true;
    std::cout << b << std::endl;

    b = false;
    std::cout << b << std::endl;

    b = 12342;
    std::cout << b << std::endl;
    b = -12342;
    std::cout << b << std::endl;
    b = 0;
    std::cout << b << std::endl;
    return 0;
}