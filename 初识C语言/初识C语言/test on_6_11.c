//移位操作符
//作用于二进制位
// << - 左移操作符 - 二进制左移一位即十进制乘以二
// >> - 右移操作符
int main()
{

	//00000000 00000000 00000000 00001100 - 12
	//00000000 00000000 00000000 00011000 - 24
	int a = 12;
	int b = a << 1;
	printf("%d\n", b);

	//00000000 00000000 00000000 00000110 - 6
	//00000000 00000000 00000000 00001100 - 12
	int c = 6;
	int d = c << 1;
	printf("%d\n", d);

	//00000000 00000000 00000000 00000010 - 2
	//00000000 00000000 00000000 00000100 - 4
	int e = 2;
	int f = e << 1;
	printf("%d\n", f);

	//由2^0左移一位为2^1;
	//由2^1左移一位为2^2;
	//由2^2+2^3左移一位为2^3+2^4;
	
	//二进制左移一位即十进制乘以二
	return 0;
}


//位操作符
//按位与   &
//按位异或 ^
//按位或   |
//按二进制位
int main()
{
	int a = 3;
	int b = 5;
	//00000000000000000000000000000011 - a
	//00000000000000000000000000000101 - b
	//00000000000000000000000000000001 - a & b
	//对应的二进制位有0就为0，全是1则为1
	int c = a & b;
	printf("%d\n", c);//1


	//00000000000000000000000000000011 - a
	//00000000000000000000000000000101 - b
	//00000000000000000000000000000110 - a ^ b
	//对应的二进制位相同为0，相异为1
	int d = a ^ b;
	printf("%d\n", d);//6


	//00000000000000000000000000000011 - a
	//00000000000000000000000000000101 - b
    //00000000000000000000000000000111 - a | b
	//对应二进制位有1就是1，全为0才得0
	int e = a | b;
	printf("%d\n", e);//7
 

	return 0;
}


