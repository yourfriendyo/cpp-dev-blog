int main()
{
	//真与假
	//非0即真，0即假
	
	//逻辑反操作
	//真变假，假变真
	int a = 10;
	printf("%d\n", !a);//0

	int b = 0;
	printf("%d\n", !b);//1
	//假变真，规定都输出1

	//取正取负
	int c = 1;
	int d = -c;
	int e = +d;
	printf("%d\n", e);//-1,意义完全等同于数学上的正负号


	return 0;
}

int main()
{
	//sizeof计算变量所占空间大小
	int a = 10;
	printf("%d\n", sizeof(a));
	printf("%d\n", sizeof(int));


	//整数在内存中存储的时候，存储的是二进制
	//一个整数的二进制表示有3种形式：原码，反码，补码
	
	//正整数：原码，反码，补码相同；
	//负整数：原码，反码，补码需计算；

	//有符号的整数，最高位是0，表示为正，
	//              最高位是1，表示为负；
	
	//eg: 
	//  int a = 1;
	//00000000000000000000000000000001 - 原码
	//00000000000000000000000000000001 - 反码
	//00000000000000000000000000000001 - 补码

	//  int a = -1;
	//10000000000000000000000000000001 - 原码
	//11111111111111111111111111111110 - 反码 - 符号位不变，其他位按位取反
	//11111111111111111111111111111111 - 补码 - 反码+1

	//内存存储整数时，存储的是二进制的补码
	//计算时也是从补码开始计算

	//~按(二进制)位取反
	int b = 0;
	printf("%d\n", ~b);//-1

	//00000000000000000000000000000000 - 0的补码
	//11111111111111111111111111111111 - ~0的补码
	//11111111111111111111111111111110 - ~0的反码
	//10000000000000000000000000000001 - ~0的原码
	
	//故由0的补码得~0的补码，补码-1得反码，再（符号位不变）按位取反得原码；
	//最后原码代表的就是结果的二进制序列；


	return 0;

}



//++，--
int main()
{
	int a = 2;
	//a = a + 1;
	//a += 1;
	//printf("%d\n", a);

	//前置++；后置++；
	
	int c = ++a;//前置++;先++，后使用
	printf("++a=%d\n", c);//3
	printf("  a=%d\n", a);//3

	a = 2;
	int d = a++;//后置++；先使用，后++
	printf("a++=%d\n", d);//2
	printf("  a=%d\n", a);//3

	a = 2;
	int e = --a;//前置--；先--，后使用
	printf("--a=%d\n", e);//1
	printf("  a=%d\n", a);//1

	a = 2;
	int f = a--;//后置--；先使用，后--
	printf("a--=%d\n", f);//2
	printf("  a=%d\n", a);//1
	

	return 0;
}

